function out = fit_once(y, s, N, K)
% FIT_ONCE  Fit a single (N,K) model by QR-based least squares (cos-only).

    [A,b,meta] = build_design(y, s, N, K);
    beta = qr_solve_dense(A, b);

    res  = A*beta - b;
    RSS  = res.'*res;

    coef = unpack_coeffs(beta, N, K);

    % Enforce column vectors
    coef.c     = coef.c(1);
    coef.a     = coef.a(:);
    coef.alpha = coef.alpha(:);
    coef.beta  = coef.beta(:);  %#ok<NASGU> 

    % === Autograder expects d = [c; a; alpha] (length 1+N+K) ===
    coef.d = [coef.c; coef.a; coef.alpha];

    out = struct( ...
        'beta', beta(:), ...
        'coef', coef, ...
        'RSS' , RSS, ...
        'M'   , meta.rows, ...
        'p'   , meta.p, ...
        'N'   , N, ...
        'K'   , K, ...
        's'   , s, ...
        'res' , res(:), ...
        'd'   , coef.d ...
    );
end
